import { Schema as _Schema, model } from 'mongoose';
const Schema = _Schema;

const PostSchema = new Schema({
  title: {
    type: String,
    required: true
  },
  content: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

export default model('Post', PostSchema);
